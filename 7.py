# ¿Si la lista 1 es [4, 6, 8, 1, 0, 3], que sería la lista 1 [-1]?

lista = [4, 6, 8, 1, 0, 3]
print (lista[-1])

# Este código muestra cómo acceder al último elemento de una lista en Python.
# La lista 'lista' contiene seis elementos: [4, 6, 8, 1, 0, 3].
# Al utilizar el índice -1, se accede al último elemento de la lista, 
# que en este caso es '3'.
# Por lo tanto, el resultado de print(lista[-1]) será '3'.
